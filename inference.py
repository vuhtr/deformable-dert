# standard PyTorch mean-std input image normalization
transform = T.Compose([
    T.Resize(800),
    T.ToTensor(),
    T.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225])
])

def main(args):
    utils.init_distributed_mode(args)
    print("git:\n  {}\n".format(utils.get_sha()))

    device = torch.device(args.device)

    # fix the seed for reproducibility
    seed = args.seed + utils.get_rank()
    torch.manual_seed(seed)
    np.random.seed(seed)
    random.seed(seed)

    model, criterion, postprocessors = build_model(args)
    model.to(device)
    checkpoint = torch.load(args.resume, map_location='cpu')
    model.load_state_dict(checkpoint['model'], strict=False)
    if torch.cuda.is_available():
        model.cuda()
    model.eval()

    output_folder = args.output_dir
    os.makedirs(output_folder, exist_ok=True)

    img_names = os.listdir(args.input_dir)
    for name in tqdm(img_names):
        path = os.path.join(args.input_dir, name)

        # mean-std normalize the input image (batch-size: 1)
        im = Image.open(path)
        img = transform(im).unsqueeze(0)
        img=img.cuda()

        # propagate through the model
        outputs = model(img)

        out_logits, out_bbox = outputs['pred_logits'], outputs['pred_boxes']
        prob = out_logits.sigmoid()
        topk_values, topk_indexes = torch.topk(prob.view(out_logits.shape[0], -1), 100, dim=1)
        scores = topk_values
        topk_boxes = topk_indexes // out_logits.shape[2]
        labels = topk_indexes % out_logits.shape[2]
        boxes = box_ops.box_cxcywh_to_xyxy(out_bbox)
        boxes = torch.gather(boxes, 1, topk_boxes.unsqueeze(-1).repeat(1,1,4))

        # filter boxes with conf < thres
        keep = scores[0] > args.conf_thres
        boxes = boxes[0, keep]
        labels = labels[0, keep]

        # relative [0, 1] to absolute [0, height] coordinates
        im_h, im_w = im.size
        target_sizes = torch.tensor([[im_w, im_h]])
        target_sizes = target_sizes.cuda()
        img_h, img_w = target_sizes.unbind(1)
        scale_fct = torch.stack([img_w, img_h, img_w, img_h], dim=1)
        boxes = boxes * scale_fct[:, None, :]

        # save results
        source_img = Image.open(path).convert("RGBA")
        draw = ImageDraw.Draw(source_img)
        for xmin, ymin, xmax, ymax in boxes[0].tolist():
            draw.rectangle(((xmin, ymin), (xmax, ymax)), outline ="red")
        source_img.save(os.path.join(output_folder, name), "png")

        # results = [{'conf': s, 'class': l, 'box': b} for s, l, b in zip(scores, labels, boxes)]

if __name__ == '__main__':
    parser = get_args_parser()
